type MondayLogoSvgProps = SVGMotionProps<SVGSVGElement> & {
  autoScramble?: boolean;
};

const MondayLogoSvg = ({ autoScramble = false, ...props }: MondayLogoSvgProps) => {
  const [index, setIndex] = useState(0);
  const intervalRef = useRef<NodeJS.Timeout | null>(null);

  const runScramble = (infinite = false) => {
    let i = 0;

    if (intervalRef.current) clearInterval(intervalRef.current);

    const totalSteps = SYMBOLS.length * 3;

    intervalRef.current = setInterval(() => {
      setIndex(Math.floor(Math.random() * SYMBOLS.length));
      i++;

      if (!infinite && i >= totalSteps) {
        if (intervalRef.current) {
          clearInterval(intervalRef.current);
          intervalRef.current = null;
        }
        setIndex(0);
      }
    }, 120);
  };

  const stopScramble = () => {
    if (intervalRef.current) {
      clearInterval(intervalRef.current);
      intervalRef.current = null;
    }
    setIndex(0);
  };

  const startScramble = () => runScramble(false);

  // Auto mode
  useEffect(() => {
    if (autoScramble) {
      runScramble(true);
    }

    return () => {
      if (intervalRef.current) clearInterval(intervalRef.current);
    };
  }, [autoScramble]);

  return (
    <motion.svg
      width={44}
      height={44}
      viewBox="0 0 44 44"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      {...props}
      onHoverStart={!autoScramble ? startScramble : undefined}
      onHoverEnd={!autoScramble ? stopScramble : undefined}
      onTapStart={!autoScramble ? startScramble : undefined}
      onTapCancel={!autoScramble ? stopScramble : undefined}
      style={{ cursor: "pointer", touchAction: "manipulation" }}
    >
      <g id="logofigure">
        <g id="ico">
          <circle
            id="ellipse"
            cx={22}
            cy={22}
            r={22}
            className={styles.fpath}
          />
          <motion.g id="circle">
            {SYMBOLS.map((symbol, i) => (
              <motion.g
                key={i}
                initial={{ opacity: i === 0 ? 1 : 0 }} // Show only first symbol initially
                animate={{ opacity: i === index ? 1 : 0 }}
                transition={{ duration: 0.08, ease: "linear" }}
              >
                {symbol}
              </motion.g>
            ))}
          </motion.g>
        </g>
      </g>
    </motion.svg>
  );
};
export default MondayLogoSvg;
